<template>
  <nav aria-label="Page navigation example">
    <ul class="pagination">
      <li class="page-item"  :class="{'disabled': pages.has_pre == false}">
        <a class="page-link" href="#" aria-label="Previous" @click.prevent="gotoParent(pages.current_page-1)">
          <span aria-hidden="true">&laquo;</span>
        </a>
      </li>
      <li class="page-item" v-for="page in pages.total_pages" :key="page" :class="{'active': pages.current_page == page}">
        <a class="page-link" href="#" @click.prevent="gotoParent(page)">{{page}}</a>
      </li>
      <li class="page-item" :class="{'disabled': pages.has_next == false}">
        <a class="page-link" href="#" aria-label="Next" @click.prevent="gotoParent(pages.current_page+1)">
          <span aria-hidden="true">&raquo;</span>
        </a>
      </li>
    </ul>
  </nav>
</template>

<script>
export default {
    name: 'pagination',
    props: ["pages"],
    methods: {
        gotoParent: function(page){
            this.$emit("fromChild", page);
        }
    }
}
</script>